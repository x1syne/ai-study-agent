/**
 * Определение домена по теме и курсу
 *
 * Умный алгоритм с весами, контекстным анализом и защитой от ложных срабатываний.
 * Ключевой принцип: PROGRAMMING — это написание кода, а не использование инструментов/приложений.
 * Любой инструмент/сервис/платформа без написания кода → GENERAL или другой подходящий домен.
 */
export function detectDomain(topic: string, courseName: string): DomainType {
  const text = `${topic} ${courseName}`.toLowerCase()
  
  // ── 1. Точное совпадение для чётких предметных областей ──────────────────
  // Явные индикаторы написания кода (должны быть ДО обычного keyword matching)
  const CODING_INDICATORS = [
    'написание кода', 'разработка приложений', 'разработка сайтов',
    'программирование на', 'изучение python', 'изучение javascript',
    'изучение java', 'изучение c++', 'алгоритмы и структуры данных',
    'объектно-ориентированное', 'функциональное программирование',
    'веб-разработка', 'мобильная разработка', 'backend разработка',
    'frontend разработка', 'fullstack'
  ]
  if (CODING_INDICATORS.some(ind => text.includes(ind))) {
    return 'programming'
  }

  // ── 2. Явные NOT-PROGRAMMING индикаторы (инструменты, сервисы, платформы) ─
  // Эти темы — использование готовых продуктов, а не написание кода
  const TOOL_INDICATORS = [
    // Паттерны "работа с инструментом / использование сервиса / настройка платформы"
    'работа с', 'настройка', 'использование', 'обучение работе',
    'автоматизация без', 'автоматизация с помощью',
    'визуальный', 'визуальная', 'no-code', 'nocode', 'low-code', 'lowcode',
    'без программирования', 'без кода',
  ]
  const hasToolPattern = TOOL_INDICATORS.some(ind => text.includes(ind))

  // ── 3. Keyword scoring с весами ──────────────────────────────────────────
  const scores: Partial<Record<DomainType, number>> = {}
  
  for (const [domain, keywords] of Object.entries(DOMAIN_KEYWORDS)) {
    if (domain === 'general') continue
    
    let score = 0
    for (const keyword of keywords) {
      if (text.includes(keyword)) {
        // Полное слово даёт больше очков
        const isWholeWord = new RegExp(`(^|\\s|-)${keyword.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}($|\\s|-)`, 'i').test(text)
        score += isWholeWord ? 2 : 1
      }
    }
    
    if (score > 0) {
      scores[domain as DomainType] = score
    }
  }

  // ── 4. Штраф для programming если явный паттерн "инструмент" ────────────
  if (hasToolPattern && scores['programming']) {
    // Если тема похожа на использование инструмента — снижаем вес programming
    scores['programming'] = Math.floor(scores['programming'] * 0.3)
  }

  // ── 5. Выбираем победителя ───────────────────────────────────────────────
  let bestMatch: DomainType = 'general'
  let maxScore = 0
  
  for (const [domain, score] of Object.entries(scores)) {
    if (score > maxScore) {
      maxScore = score
      bestMatch = domain as DomainType
    }
  }

  // ── 6. Минимальный порог: если очень мало совпадений — это GENERAL ────────
  if (maxScore <= 1) {
    return 'general'
  }

  return bestMatch
}


