/**
 * Определение домена по теме и курсу
 */
export function detectDomain(topic: string, courseName: string): DomainType {
  const text = `${topic} ${courseName}`.toLowerCase()
  
  let bestMatch: DomainType = 'general'
  let maxScore = 0
  
  for (const [domain, keywords] of Object.entries(DOMAIN_KEYWORDS)) {
    if (domain === 'general') continue
    
    let score = 0
    for (const keyword of keywords) {
      if (text.includes(keyword)) {
        score++
      }
    }
    
    if (score > maxScore) {
      maxScore = score
      bestMatch = domain as DomainType
    }
  }
  
  return bestMatch
}


