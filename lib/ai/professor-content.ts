// Все публикации с контентом
export const ALL_PUBLICATION_CONTENTS: PublicationContent[] = [
  AI_SYSTEMS_CONTENT,
  IT_MANAGEMENT_CONTENT,
  IS_DESIGN_CONTENT
]

/**
 * Получить весь контент для индексации
 */
export function getAllContentForIndexing(): { 
  publicationId: string
  title: string
  content: string
  keywords: string[]
}[] {
  const allContent: { publicationId: string; title: string; content: string; keywords: string[] }[] = []
  
  for (const pub of ALL_PUBLICATION_CONTENTS) {
    for (const chapter of pub.chapters) {
      allContent.push({
        publicationId: pub.publicationId,
        title: chapter.title,
        content: chapter.content,
        keywords: chapter.keywords
      })
    }
  }
  
  return allContent
}
